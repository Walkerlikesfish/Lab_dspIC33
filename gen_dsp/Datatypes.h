//                       DATA TYPES adapted to Compiler C30
//*********************************************************************************************************
#ifndef _datatypes_
	#define _datatypes_

	typedef unsigned char  		INT8U;		/* Unsigned  8 bit integer                           */
	typedef signed   char  		INT8S;      /* Signed    8 bit integer                           */
	typedef unsigned int   		INT16U;     /* Unsigned 16 bit integer                           */
	typedef signed   int   		INT16S;     /* Signed   16 bit integer                           */
	typedef unsigned long  		INT32U;     /* Unsigned 32 bit integer                           */
	typedef signed   long  		INT32S;  	/* Signed   32 bit integer						   */	
	typedef unsigned long long  INT64U;     /* Unsigned 64 bit integer                           */
	typedef signed   long long  INT64S;     /* Signed   64 bit integer                           */
	typedef float 				FP32;       /* Single precision floating point                    */
	typedef long double 		FP64;       /* Double precision floating point                    */

#endif
